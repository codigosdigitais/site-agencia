               <div class="object-lg-right">
                  <h4>Etapas do Desenvolvimento de Aplicativo</h4>
                  <p class="text-justify"><b>Primeira Etapa:</b> Como qualquer processo, a primeira etapa do desenvolvimento de aplicativo é o planejamento. Neste momento, é criado o escopo, ou seja, as principais informações que determinam a finalidade do app. É a partir disso que pode-se analisar a sua funcionalidade e criar protótipos para avaliar o projeto e o design. Portanto, o planejamento é uma fase de extrema importância, visto que o funcionamento e gerenciamento do aplicativo dependem dessa fase. É importante o desenvolvimento de um bom "wireframe" (desenho do aplicativo de forma simples e contextualizada), para que o cliente vislumbre e determine a forma que deseja que seu aplicativo funcione. </p>

                  <p class="text-justify"><b>Segunda Etapa:</b> Feito o planejamento, o próximo passo é o desenvolvimento do projeto, em outras palavras, começa a parte técnica do aplicativo. Trata-se de uma etapa mais complexa, por tabela, mais extensa.</p>

                  <p class="text-justify"><b>Terceira Etapa:</b> Quando o desenvolvimento é finalizado, inicia-se a fase de testes. Essa parte também é muito importante, uma vez que colabora para o app ser lançado sem falhas. Aqui, o usuário avalia o aplicativo para que a empresa compreenda a sua satisfação. Um prazo bacana para teste é um mês.</p>

                  <p class="text-justify"><b>Etapa Final:</b> Testes feitos e aplicativo aprovado? Então deve-se planejar o seu lançamento. Quer publicá-lo nas principais lojas como a Google Play e Apple Store? É possível, contanto que o app siga as políticas de cada loja.</p>
